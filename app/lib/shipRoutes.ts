export function getShipRouteInfo(shipName: string): ShipRouteInfo {
  // 정확히 매치되는 게 없으면 기본값 반환
  const cleanedName = shipName.replace(/\s+/g, '');
  for (const [key, value] of Object.entries(shipRouteMap)) {
    if (cleanedName.includes(key) || key.includes(cleanedName)) {
      return value;
    }
  }
  return {
    route: '목포항 기항 노선',
    ports: ['목포', '목포항']
  };
}
